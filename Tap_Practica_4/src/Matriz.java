        int A[][]=new int[n][n];
        for(int r=0;r<A.length;r++){
            for(int c=0;c<A.length;c++){
                A[r][c]=(int)(Math.random()*101);
             }
         }
         m.setRowCount(A.length);//cantidad de filas
         m.setColumnCount(A.length);
         for(int r=0;r<A.length;r++){
         for(int c=0;c<A.length;c++){
         m.setValueAt(A[r][c], r, c);
              }
         }
             
     
